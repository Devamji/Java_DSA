package Basic;
import java.util.*;
public class RELATIONAL_OPERATER {
    public static void main(String[] args) {
        
        // cerate a variable 
        Scanner sc = new Scanner (System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        System.out.println(" a is "+a+"and b is "+b);

        // IT'S GIVE BOOLEAN VALUE IN RETURN
        // == Operater
        System.out.println(a==b); 

        // != Operater
        System.out.println(a!=b);

        // > Operater
        System.out.println(a>b);

        // < Operater
        System.out.println(a<b);

        // >= Operater
        System.out.println(a>=b);

        // <= Operater
        System.out.println(a<=b);


    }
}
